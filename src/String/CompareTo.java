package String;
/**
 * compareTo() method will check the string content lexicographical it converts character to there specific unicode value and returns the
 * integer value.
 * if strings are containing same content it will return zero
 *
 */

public class CompareTo {
    public static void main(String[] args) {
        String s1="apple";
        String s2="animal";
        System.out.println(s2.compareTo(s1));
    }
}
